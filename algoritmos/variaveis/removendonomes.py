
# Removendo nomes: Use uma variável para representar o nome de uma pessoa e
#  inclua alguns caracteres de espaço em branco no início e no final do nome.
# Lembre-se de usar cada combinação de caracteres, "\t" e "\n", pelo menos uma
# vez.Exiba o nome uma vez para que o espaço em branco ao redor do nome seja
# mostrado. Em seguida, printe o nome usando cada uma das três funções de
# remoção, lstrip(), rstrip(), e strip().
name = " ayam marto "
print(name)
print(name.lstrip())
print(name.rstrip())
print(name.strip())
print("\n" + name)
print("\t" + name)
