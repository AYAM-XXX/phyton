# 2.4 Maiúsculas e minúsculas: Use uma variável para representar o nome de uma
# pessoa e, em seguida, exiba o nome dessa pessoa em letras minúsculas,
# maiúsculas e as primeiras letras maiúsculas.

first_name = "ayam"
last_name = "marto"
name = f'{first_name} {last_name}'
print(name.title())  # coloca o titulo
print(name.upper())  # deixa tudo em maiusculo
print(name.lower())  # deixa tudo em minusculo
